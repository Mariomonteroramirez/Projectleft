
divisor = 2

basetriangulo = input("Ingresa la base de el triangulo :")
basetriangulo = float(basetriangulo)

alturatriangulo = input("Ingresa la altura de el triangulo :")
alturatriangulo = float(alturatriangulo)

result = basetriangulo * alturatriangulo / divisor

print("El area deun triangulo es : ",result)
